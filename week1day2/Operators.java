package week1day2;

public class Operators {

	public static void main(String[] args) {
		int a=10;
		System.out.println(10+10);
		System.out.println(10-10);
		System.out.println(10/10);
		System.out.println(10%5);
		System.out.println(10*10);
		System.out.println(10==10);
		System.out.println(10<10);
		System.out.println(10<=10);
		System.out.println(a++);
		System.out.println(a--);
		System.out.println(a);
		System.out.println((5==5)&&(5<5));
		System.out.println((5==5)||(5<5));
				System.out.println(!(5==5));
		

	}

}
